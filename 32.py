#Эта функция find_indexes принимает три аргумента: список arr, 
#минимальное значение min_val и максимальное значение max_val, 
#и возвращает список индексов элементов списка, значение которых находится в заданном диапазоне.

#Функция использует цикл for для итерации по элементам списка и проверки, 
#находится ли значение элемента в заданном диапазоне. 
#Если это так, то индекс элемента добавляется в список indexes. Наконец, функция возвращает список indexes.

def find_indexes(arr, min_val, max_val):
    indexes = []
    for i in range(len(arr)):
        if min_val <= arr[i] <= max_val:
            indexes.append(i)
    return indexes

#Вы можете вызвать эту функцию, передав список, минимальное и максимальное значения в качестве аргументов, например:
arr = [1, 2, 3, 4, 5, 6, 7, 8, 9]
min_val = 3
max_val = 7
indexes = find_indexes(arr, min_val, max_val)
print(indexes)

#Этот код создает список arr и задает минимальное значение min_val равным 3 и максимальное значение max_val равным 7.
#  Затем он вызывает функцию find_indexes и передает ей список и заданные значения. 
# Наконец, он выводит список индексов, которые удовлетворяют заданному диапазону.